
#Se captura el radio, se convierte en tipo float y lo asigna a la variable radio
radio = float(input("Captura el radio de un circulo: ") )

#Se realiza la operacion para obtener el area
area = 3.1416 * (radio * radio)

#Se muestra el resultado por medio de la funcion print con formato
print(f"El area del circulo es: {area}")